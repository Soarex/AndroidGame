package com.alessio.engine.math;

public class Float4 {
    public float x, y, z, w;

    public Float4() {
        x = 0;
        y = 0;
        z = 0;
        w = 0;
    }

    public Float4(float x, float y, float z, float w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
}
